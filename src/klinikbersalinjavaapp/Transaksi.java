        public final void setJam(){
        ActionListener taskPerformer = new ActionListener() {
            @Override
            public void actionPerformed(ActionEvent e) {
                String jam = "", menit = "", detik = "";
                
                java.util.Date Waktu = new java.util.Date();
                int set_jam = Waktu.getHours();
                int set_menit = Waktu.getMinutes();
                int set_detik = Waktu.getSeconds();
                
                if(set_jam <= 9) jam= "0";
                if(set_menit <= 9) menit= "0";
                if(set_detik <= 9) detik= "0";
                
                String hours = jam + Integer.toString(set_jam);
                String minutes = menit + Integer.toString(set_menit);
                String seconds = detik + Integer.toString(set_detik);
                
                wkt1.setText(hours+":"+minutes+":"+seconds);
            }
        };
        new Timer(1000, taskPerformer).start();
    }

