    private void InputNoTransaksiActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_InputNoTransaksiActionPerformed
        // TODO add your handling code here:
        try {
            Class.forName("com.mysql.jdbc.Driver").newInstance();
            com.mysql.jdbc.Connection conn = (com.mysql.jdbc.Connection) DriverManager.getConnection(
                    "jdbc:mysql://localhost:3306/klinik_db", "root", "");
            com.mysql.jdbc.Statement state = (com.mysql.jdbc.Statement) conn.createStatement();
            String sql = "SELECT * FROM pembayaran order by no_trans";
            ResultSet view = state.executeQuery(sql);
            if (view.first() == false) {
                InputNoTransaksi.setText("000001");
            } else {
                view.last();
                int no = view.getInt(1) + 1;
                String cno=String.valueOf(no);
                int pjg_cno=cno.length();
                for (int i=0;i<3-pjg_cno;i++)
                {
                cno="0"+cno;
                }
                InputNoTransaksi.setText("000"+cno);

            }
            state.close();
            conn.close();
        } catch (ClassNotFoundException | InstantiationException | IllegalAccessException | SQLException | HeadlessException e) {
            JOptionPane.showMessageDialog(null, "Eror:" + e, "Gagal", JOptionPane.WARNING_MESSAGE);
//System.err.println("Exception: "+e.getMessage());
        }
    }//GEN-LAST:event_InputNoTransaksiActionPerformed

